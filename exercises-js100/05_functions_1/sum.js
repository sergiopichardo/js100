/*
Examine the example function invocation below.
Write the function sum, such that it accepts two
arguments and returns the sum of its arguments.
You may assume that the function arguments will
always be numbers.

sum(22, 10); // 32

*/

/**
 * @param {number} num1
 * @param {number} num2
 * @return {number}
 */
function sum(num1, num2) {
  return num1 + num2;
}

sum(22, 10); // 32

